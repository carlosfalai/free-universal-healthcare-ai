'use strict';
/**
 * pdf-generator.js — renders the ---DOC:TYPE--- sections the clinical-reasoning
 * model emits (SOAP, REFERRAL, PRESCRIPTION, IMAGING, LABS, WORKLEAVE) into
 * clean LETTER-size PDFs, and delivers them over Telegram.
 *
 * These are EDUCATIONAL mock documents ("what such a document would look like"),
 * never real signed clinical records — every page carries that disclaimer and
 * no signature block.
 *
 * Exports (contract expected by bot.js):
 *   generateDocumentsFromAssessment(patientMessage, summary, hpiSummary,
 *                                   documentsText, language='en')
 *       -> Promise<Array<{ path, title, type }>>   (caller reads f.path/f.title
 *          then unlinks — see the /web/assess route and sendDocuments below)
 *   sendDocuments(bot, chatId, pdfFiles, language='en') -> Promise<void>
 *       (sends each file to the chat, then removes the temp file)
 */
const fs = require('fs');
const os = require('os');
const path = require('path');
const PDFDocument = require('pdfkit');

// DOC:TYPE -> human title on the page + the Telegram filename.
const DOC_TITLES = {
  SOAP: 'SOAP Note',
  REFERRAL: 'Referral Note',
  PRESCRIPTION: 'Prescription (Educational Sample)',
  IMAGING: 'Imaging Requisition',
  LABS: 'Laboratory Requisition',
  WORKLEAVE: 'Work Leave Certificate',
};
// Sensible reading order when several documents are produced for one case.
const DOC_ORDER = ['SOAP', 'REFERRAL', 'PRESCRIPTION', 'IMAGING', 'LABS', 'WORKLEAVE'];

const DISCLAIMER =
  'EDUCATIONAL USE ONLY. Generated by InstantHPI AI from patient-reported ' +
  'information. This is a demonstration of what such a document could look like; ' +
  'it is NOT a diagnosis, NOT a valid prescription, and does NOT replace ' +
  'assessment by a licensed physician. No physician has reviewed or signed this document.';

const SEND_CAPTIONS = {
  en: 'Educational document — not a real prescription or medical record.',
  fr: 'Document éducatif — ni une vraie ordonnance ni un dossier médical.',
  es: 'Documento educativo — no es una receta ni un registro médico real.',
  pt: 'Documento educativo — não é uma receita nem um registo médico real.',
  hi: 'Educational document — not a real prescription or medical record.',
  ar: 'Educational document — not a real prescription or medical record.',
};

/**
 * Split the raw ---DOCS--- payload into { type, body } sections keyed by the
 * ---DOC:TYPE--- markers. Body runs from one marker to the next (or to the end).
 */
function parseDocuments(documentsText) {
  if (!documentsText || typeof documentsText !== 'string') return [];
  const re = /---DOC:([A-Z]+)---/g;
  const marks = [];
  let m;
  while ((m = re.exec(documentsText)) !== null) {
    marks.push({ type: m[1], start: m.index, contentStart: m.index + m[0].length });
  }
  const sections = [];
  for (let i = 0; i < marks.length; i++) {
    const end = i + 1 < marks.length ? marks[i + 1].start : documentsText.length;
    const body = documentsText.slice(marks[i].contentStart, end).trim();
    if (body) sections.push({ type: marks[i].type, body });
  }
  return sections;
}

function safeFileName(section) {
  const base = (DOC_TITLES[section.type] || section.type).replace(/[^A-Za-z0-9]+/g, '-').replace(/^-|-$/g, '');
  return `InstantHPI-${base}.pdf`;
}

/** Render one document section to a temp PDF; resolves once fully flushed to disk. */
function renderPdf(section) {
  return new Promise((resolve, reject) => {
    const type = section.type;
    const title = DOC_TITLES[type] || (type.charAt(0) + type.slice(1).toLowerCase());
    const file = path.join(
      os.tmpdir(),
      `instanthpi-${type.toLowerCase()}-${Date.now()}-${Math.floor(Math.random() * 1e6)}.pdf`,
    );

    const doc = new PDFDocument({
      size: 'LETTER',
      margin: 56,
      info: { Title: title, Author: 'InstantHPI (educational)' },
    });
    const stream = fs.createWriteStream(file);
    let settled = false;
    const fail = (e) => { if (!settled) { settled = true; reject(e); } };
    doc.on('error', fail);
    stream.on('error', fail);
    stream.on('finish', () => { if (!settled) { settled = true; resolve({ path: file, title, type }); } });

    doc.pipe(stream);

    // ── letterhead ──
    doc.font('Helvetica-Bold').fontSize(16).fillColor('#0F172A').text('InstantHPI');
    doc.font('Helvetica').fontSize(9).fillColor('#64748B')
      .text('AI-assisted educational medical document — not a real clinical record');
    doc.moveDown(0.5);
    const left = doc.page.margins.left;
    const right = doc.page.width - doc.page.margins.right;
    doc.strokeColor('#CBD5E1').lineWidth(1).moveTo(left, doc.y).lineTo(right, doc.y).stroke();
    doc.moveDown(0.9);

    // ── document title ──
    doc.font('Helvetica-Bold').fontSize(15).fillColor('#0F172A').text(title.toUpperCase());
    doc.moveDown(0.6);

    // ── body (line breaks preserved for Rx / labs) ──
    doc.font('Helvetica').fontSize(11).fillColor('#111827').text(section.body, { align: 'left', lineGap: 3 });
    doc.moveDown(1.4);

    // ── disclaimer footer ──
    doc.strokeColor('#E2E8F0').lineWidth(0.5).moveTo(left, doc.y).lineTo(right, doc.y).stroke();
    doc.moveDown(0.5);
    doc.font('Helvetica-Oblique').fontSize(8).fillColor('#991B1B').text(DISCLAIMER, { lineGap: 1 });
    doc.moveDown(0.3);
    doc.font('Helvetica').fontSize(8).fillColor('#94A3B8')
      .text('Generated ' + new Date().toISOString().slice(0, 10) + ' · t.me/InstantHPIBot');

    doc.end();
  });
}

/**
 * Build a PDF for every ---DOC:TYPE--- section in `documentsText`.
 * A failed render is logged and skipped rather than aborting the batch.
 */
async function generateDocumentsFromAssessment(patientMessage, summary, hpiSummary, documentsText, language = 'en') {
  const sections = parseDocuments(documentsText);
  sections.sort((a, b) => {
    const ia = DOC_ORDER.indexOf(a.type); const ib = DOC_ORDER.indexOf(b.type);
    return (ia < 0 ? 99 : ia) - (ib < 0 ? 99 : ib);
  });
  const out = [];
  for (const s of sections) {
    try {
      out.push(await renderPdf(s));
    } catch (e) {
      console.error('PDF render failed for', s.type + ':', e.message);
    }
  }
  return out;
}

/** Send each generated PDF to the Telegram chat, then clean up the temp file. */
async function sendDocuments(bot, chatId, pdfFiles, language = 'en') {
  const caption = SEND_CAPTIONS[language] || SEND_CAPTIONS.en;
  for (const f of pdfFiles || []) {
    try {
      await bot.sendDocument(
        chatId,
        f.path,
        { caption: `📄 ${f.title} — ${caption}` },
        { filename: safeFileName(f), contentType: 'application/pdf' },
      );
    } catch (e) {
      console.error('sendDocument failed for', f.title + ':', e.message);
    } finally {
      try { fs.unlinkSync(f.path); } catch (_) { /* already gone */ }
    }
  }
}

module.exports = { generateDocumentsFromAssessment, sendDocuments, parseDocuments };
